class Percentage{
public static void main(String args[]){
int a=99;
if(a<35){
System.out.println("'E' Grade.");
}else if(a>35 && a<45){
System.out.println("'D' Grade");
}else if(a>45 && a<60){
System.out.println("'C' Grade");
}else if(a>60 && a<75){
System.out.println("'B' Grade");
}else if(a>75 && a<90){
System.out.println("'A' Grade");
}else{
System.out.println("'A+' Grade");
}}}